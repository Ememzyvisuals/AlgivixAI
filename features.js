function recordMood(phone, text) {
  const lower = text.toLowerCase();
  let mood    = null;
  if (lower.includes("1") || lower.includes("great") || lower.includes("good") || lower.includes("amazing")) mood = "😊 great";
  else if (lower.includes("2") || lower.includes("okay") || lower.includes("fine")) mood = "😐 okay";
  else if (lower.includes("3") || lower.includes("bad") || lower.includes("sad") || lower.includes("stressed")) mood = "😔 struggling";
  if (mood) {
    memory.moodHistory.push({ phone, mood, time: Date.now() });
    return mood;
  }
  return null;
}

function getMoodSummary() {
  if (memory.moodHistory.length === 0) return `💭 No mood data yet. Use ${fmt("!mood")} to check in!`;
  const counts = { great: 0, okay: 0, struggling: 0 };
  memory.moodHistory.forEach(m => {
    if (m.mood.includes("great")) counts.great++;
    else if (m.mood.includes("okay")) counts.okay++;
    else counts.struggling++;
  });
  const total = memory.moodHistory.length;
  return (
    `💭 ${fmt("Team Mood Summary")}\n${divider()}\n` +
    `😊 Great: ${counts.great} (${Math.round(counts.great/total*100)}%)\n` +
    `😐 Okay: ${counts.okay} (${Math.round(counts.okay/total*100)}%)\n` +
    `😔 Struggling: ${counts.struggling} (${Math.round(counts.struggling/total*100)}%)\n\n` +
    `${counts.struggling > 0 ? `💙 ${fmt("Some members need support — check on them!")}` : `🔥 ${fmt("Team morale is strong!")}`}`
  );
}

// ─── Secret Admin Commands ────────────────────────────────────────────────────
function handleSecretCommand(text, phone) {
  const lower = text.toLowerCase().trim();

  if (lower === "nuke warnings") {
    memory.warnings.clear();
    return `💥 ${fmt("DONE!")} All warnings have been cleared! Everyone gets a fresh start. 🔄`;
  }

  if (lower === "ghost mode") {
    memory.ghostMode = !memory.ghostMode;
    return memory.ghostMode
      ? `👻 ${fmt("Ghost Mode ON")} — I'll go silent for 1 hour. Watching but not responding...`
      : `👋 ${fmt("Ghost Mode OFF")} — I'm back! Miss me? 😄`;
  }

  if (lower === "hype mode") {
    memory.hypeMode = !memory.hypeMode;
    return memory.hypeMode
      ? `🔥🔥🔥 ${fmt("HYPE MODE ACTIVATED!!!")} LET'S GOOOOO ALGIVIX DEV TEAM!!! 🚀💪🎉`
      : `😌 ${fmt("Hype Mode OFF")} — Back to professional mode.`;
  }

  if (lower === "lockdown") {
    memory.lockdown = !memory.lockdown;
    return memory.lockdown
      ? `🔐 ${fmt("LOCKDOWN MODE")} — Only admins can interact with me now.`
      : `🔓 ${fmt("Lockdown lifted")} — Everyone can interact again!`;
  }

  if (lower === "reset mvp") {
    memory.mvpVotes.clear();
    return `🔄 ${fmt("MVP scores reset!")} Starting fresh this week!`;
  }

  if (lower === "reset trivia") {
    memory.triviaScores.clear();
    return `🔄 ${fmt("Trivia scores reset!")} New competition begins!`;
  }

  if (lower === "bot stats") {
    return (
      `🤖 ${fmt("AlgivixAI Stats")}\n${divider()}\n` +
      `💬 Messages recorded: ${memory.messages.length}\n` +
      `👥 Unique members tracked: ${memory.mvpVotes.size}\n` +
      `🎮 Trivia players: ${memory.triviaScores.size}\n` +
      `💭 Mood entries: ${memory.moodHistory.length}\n` +
      `📝 Meeting notes: ${memory.meetingNotes.length}\n` +
      `👻 Ghost mode: ${memory.ghostMode ? "ON" : "OFF"}\n` +
      `🔥 Hype mode: ${memory.hypeMode ? "ON" : "OFF"}\n` +
      `🔐 Lockdown: ${memory.lockdown ? "ON" : "OFF"}`
    );
  }

  return null;
}

