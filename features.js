const { askGroq } = require("./ai");

// ─── Formatting Helpers ───────────────────────────────────────────────────────
const fmt      = t => `*${t}*`;
const italic   = t => `_${t}_`;
const divider  = () => `━━━━━━━━━━━━━━━━━━━━`;
const tagPhone = p => `@${(p || "").split("@")[0].split(":")[0]}`;

// ─── AI Memory ────────────────────────────────────────────────────────────────
const memory = {
  messages:     [],   // { phone, text, time }
  discussions:  [],   // Key topics detected
  taskAssigns:  [],   // { task, assignee, deadline, assignedBy }
  moodHistory:  [],   // { phone, mood, time }
  triviaScores: new Map(), // { phone: score }
  mvpVotes:     new Map(), // { phone: activityCount }
  warnings:     new Map(), // { phone: count }
  meetingActive: false,
  meetingNotes:  [],
  ghostMode:     false,
  hypeMode:      false,
  lockdown:      false,
};

function recordMessage(phone, text) {
  memory.messages.push({ phone, text, time: Date.now() });
  // Keep last 500 messages only
  if (memory.messages.length > 500) memory.messages.shift();

  // Track activity for MVP
  const current = memory.mvpVotes.get(phone) || 0;
  memory.mvpVotes.set(phone, current + 1);
}

// ─── Weekly Summary ───────────────────────────────────────────────────────────
async function generateWeeklySummary() {
  try {
    const recent = memory.messages.slice(-100).map(m => `${m.phone}: ${m.text}`).join("\n");
    if (!recent) return `📋 ${fmt("Weekly Summary")}\n${divider()}\nNot enough activity this week yet!`;

    const summary = await askGroq(
      `Summarize these WhatsApp group conversations in 5 bullet points. Focus on main topics, decisions made, and key discussions:\n\n${recent}`,
      "general"
    );

    return (
      `🧠 ${fmt("Weekly Discussion Summary")}\n${divider()}\n` +
      `${summary}\n\n` +
      `📊 ${fmt("Stats:")}\n` +
      `💬 Total messages: ${memory.messages.length}\n` +
      `👥 Active members: ${new Set(memory.messages.map(m => m.phone)).size}\n` +
      `📅 Generated: ${new Date().toLocaleDateString("en-US", { weekday: "long", month: "short", day: "numeric" })}`
    );
  } catch (e) {
    return `❌ Could not generate summary: ${e.message}`;
  }
}

// ─── Weekly Performance Report ────────────────────────────────────────────────
function generatePerformanceReport() {
  const activityMap = memory.mvpVotes;
  if (activityMap.size === 0) {
    return `📊 ${fmt("Weekly Performance Report")}\n${divider()}\nNo activity recorded yet this week!`;
  }

  // Sort by activity
  const sorted  = [...activityMap.entries()].sort((a, b) => b[1] - a[1]);
  const mvp     = sorted[0];
  const quiet   = sorted.filter(([, count]) => count < 3);
  const total   = [...activityMap.values()].reduce((a, b) => a + b, 0);

  let report = `📊 ${fmt("Weekly Performance Report")}\n${divider()}\n`;
  report    += `🏆 ${fmt("Most Active:")} ${tagPhone(mvp[0])} (${mvp[1]} messages)\n`;
  report    += `💬 ${fmt("Total Messages:")} ${total}\n`;
  report    += `👥 ${fmt("Active Members:")} ${activityMap.size}\n\n`;

  report += `📈 ${fmt("Activity Breakdown:")}\n`;
  sorted.slice(0, 5).forEach(([phone, count], i) => {
    const bar = "█".repeat(Math.min(count, 10));
    report   += `${i + 1}. ${tagPhone(phone)} ${bar} (${count})\n`;
  });

  if (quiet.length > 0) {
    report += `\n👀 ${fmt("Quiet Members:")}\n`;
    quiet.forEach(([phone]) => { report += `• ${tagPhone(phone)}\n`; });
  }

  report += `\n_Report generated by AlgivixAI_ 🤖`;
  return report;
}

// ─── MVP of the Week ──────────────────────────────────────────────────────────
function getMVP() {
  if (memory.mvpVotes.size === 0) return null;
  const sorted = [...memory.mvpVotes.entries()].sort((a, b) => b[1] - a[1]);
  return sorted[0];
}

