async function endMeeting() {
  if (!memory.meetingActive) return `⚠️ No meeting is currently active. Say ${fmt("start meeting")} to begin.`;
  memory.meetingActive = false;

  if (memory.meetingNotes.length === 0) {
    return `📝 ${fmt("Meeting Ended")}\nNo notes were recorded.`;
  }

  const notesText = memory.meetingNotes.map((n, i) => `${i + 1}. ${n.phone}: ${n.text}`).join("\n");

  try {
    const summary = await askGroq(
      `Summarize these meeting notes into a clean, professional meeting summary with key decisions and action items:\n\n${notesText}`,
      "general"
    );
    return (
      `📋 ${fmt("MEETING SUMMARY")}\n${divider()}\n` +
      `📅 ${new Date().toLocaleString("en-US", { timeZone: "Africa/Lagos", dateStyle: "medium", timeStyle: "short" })}\n` +
      `👥 Participants: ${new Set(memory.meetingNotes.map(n => n.phone)).size}\n` +
      `💬 Total points: ${memory.meetingNotes.length}\n\n` +
      `${summary}\n\n` +
      `${italic("Summary generated by AlgivixAI 🤖")}`
    );
  } catch {
    let summary = `📋 ${fmt("MEETING NOTES")}\n${divider()}\n`;
    memory.meetingNotes.forEach((n, i) => { summary += `${i + 1}. ${tagPhone(n.phone)}: ${n.text}\n`; });
    return summary;
  }
}

