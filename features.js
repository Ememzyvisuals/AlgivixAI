// ─── Auto Code Review ─────────────────────────────────────────────────────────
function looksLikeCode(text) {
  return [
    /```[\s\S]*```/,
    /function\s+\w+\s*\(/,
    /const\s+\w+\s*=/,
    /import\s+\S+\s+from/,
    /def\s+\w+\s*\(/,
    /class\s+\w+[\s:{]/,
    /SELECT\s+\S+\s+FROM/i,
    /console\.log\s*\(/,
    /=>\s*{/,
    /public\s+(static\s+)?void/,
    /<\/?[a-z][\w]*[\s/>]/i,
  ].some(p => p.test(text));
}

async function autoReviewCode(text) {
  try {
    const review = await askGroq(text, "code_review");
    return (
      `🔍 ${fmt("Auto Code Review by AlgivixAI")}\n${divider()}\n` +
      `${review}\n\n` +
      `${italic("Tip: Use !review <code> for manual review anytime!")}`
    );
  } catch {
    return null;
  }
}

